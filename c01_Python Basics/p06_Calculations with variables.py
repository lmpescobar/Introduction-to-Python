# Ejercicio
# Cálculos con variables

# Descripción del ejercicio:
# Ahora que has creado una variable de ahorros, ¡comencemos a ahorrar!
# En lugar de calcular con valores específicos, puedes usar variables.
# Pregunta: ¿Cuánto dinero habrás ahorrado dentro de cuatro meses si ahorras $10 cada mes?

# Instrucciones:
# 1. Crea una variable `monthly_savings` y asígnale el valor 10.
# 2. Crea una variable `num_months` y asígnale el valor 4.
# 3. Multiplica `monthly_savings` por `num_months` y guarda el resultado en la variable `new_savings`.
# 4. Muestra el valor de `new_savings` utilizando la función `print()`.

# Código en Python:
# Crear las variables monthly_savings y num_months
monthly_savings = 10  # Cantidad ahorrada cada mes, en dólares.
num_months = 4  # Número de meses.

# Calcular los ahorros totales
new_savings = monthly_savings * num_months  # Multiplicamos el ahorro mensual por el número de meses.

# Mostrar el resultado de los ahorros totales
print(new_savings)  # Imprimimos el valor de los ahorros totales.

# Explicación del código:
# - La línea 9 asigna el valor 10 a la variable monthly_savings, representando el ahorro mensual.
# - La línea 10 asigna el valor 4 a la variable num_months, indicando la cantidad de meses.
# - La línea 13 calcula el total de ahorros al multiplicar monthly_savings por num_months y almacena el resultado en new_savings.
# - La línea 16 utiliza print() para mostrar el valor de new_savings en la consola.