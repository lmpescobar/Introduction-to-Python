# Ejercicio: Python como calculadora
# Descripción:
# En este ejercicio, usaremos Python para realizar operaciones matemáticas básicas:
# suma, resta, multiplicación y división. Python es ideal para cálculos debido a su
# sintaxis simple y clara.

# Instrucciones:
# 1. Imprime la suma de 4 + 5.
# 2. Imprime el resultado de restar 5 de 5.
# 3. Imprime el resultado de multiplicar 3 por 5.
# 4. Imprime el resultado de dividir 10 entre 2.

# Código en Python:

# Suma
print(4 + 5)  # Imprime el resultado de 4 + 5, que es 9.

# Resta
print(5 - 5)  # Imprime el resultado de 5 - 5, que es 0.

# Multiplicación
print(3 * 5)  # Imprime el resultado de 3 * 5, que es 15.

# División
print(10 / 2)  # Imprime el resultado de 10 / 2, que es 5.0.

# Explicación del código:
# - Cada operación se ejecuta y su resultado se imprime inmediatamente en la consola.
# - La función `print()` se utiliza para mostrar los resultados en pantalla.
# - Este ejercicio demuestra cómo Python puede realizar operaciones matemáticas básicas.