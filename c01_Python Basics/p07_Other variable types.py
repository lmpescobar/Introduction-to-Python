# Ejercicio
# Otros tipos de variables

# Descripción del ejercicio:
# En el ejercicio anterior, trabajaste con el tipo de dato entero en Python:
# - `int` o entero: un número sin parte decimal. Por ejemplo, `savings = 100` es un ejemplo de entero.
# Ahora, exploraremos tres tipos de datos comunes en Python:
# 1. `float` o número flotante: un número que tiene parte entera y decimal, separados por un punto (ejemplo: 1.1).
# 2. `str` o cadena de texto: un tipo para representar texto. Las cadenas se pueden crear con comillas simples o dobles.
# 3. `bool` o valor booleano: un tipo para representar valores lógicos. Solo puede ser `True` o `False`.
# Nota: la capitalización es importante en los valores booleanos.

# Instrucciones:
# 1. Crea una nueva variable flotante llamada `half` con el valor `0.5`.
# 2. Crea una nueva cadena de texto llamada `intro` con el valor `"Hello! How are you?"`.
# 3. Crea un nuevo valor booleano llamado `is_good` con el valor `True`.

# Código en Python:
# Crear una variable half
half = 0.5  # Asignamos un número flotante que representa la mitad.

# Crear una variable intro
intro = "Hello! How are you?"  # Asignamos una cadena de texto para representar un saludo.

# Crear una variable is_good
is_good = True  # Asignamos un valor booleano que indica algo verdadero.

# Explicación del código:
# - La línea 9 asigna un número flotante (0.5) a la variable half.
# - La línea 12 asigna una cadena de texto a la variable intro utilizando comillas dobles.
# - La línea 15 asigna un valor booleano `True` a la variable is_good, representando un estado lógico verdadero.